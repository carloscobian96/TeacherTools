class RandomApp:
    def __init__(self, id: int, uid: str, app_name: str, app_version: str, app_author: str, app_semantic_version: str, app_major_version: str, app_minor_version: str, app_patch_version: str):
        self.id = id
        self.uid = uid
        self.app_name = app_name
        self.app_version = app_version
        self.app_author = app_author
        self.app_semantic_version = app_semantic_version
        self.app_major_version = app_major_version
        self.app_minor_version = app_minor_version
        self.app_patch_version = app_patch_version